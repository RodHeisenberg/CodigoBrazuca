temperatura_celsius = float(input("Digite a temperatura em °C: "))

temperatura_kelvin = (temperatura_celsius + 273.15)
print(f"A temperatura de Celsius convertida em Kelvin é {temperatura_kelvin}")
temperatura_fahrenheit = (temperatura_celsius * 9/5) + 32
print(f"A temperatura de Celsius convertida em Fahrenheit é {temperatura_fahrenheit}")
